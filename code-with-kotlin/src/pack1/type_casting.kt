//type conversion is called type casting
//small data type to big data type
/*
toByte() toInt(), toLong(), toFloat, toDouble,toShort
*/
fun main(){
   var v:Int=123456
   var l:Long=v.toLong()
   var d:Double= v.toDouble()
    println(v)
    println(l)
    println(d)

}